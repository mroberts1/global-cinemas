	<h1>About</h1>

	<h2>What this is</h2>
	<p>
		<strong>Global Cinemas</strong> is a visual atlas of film movements — those moments in cinema history
		where a generation of filmmakers, often clustered in one country or city, broke from what came before.
		From the <em>Cinema of Attractions</em> in 1895 to today's <em>New Extremity</em>, cinema has moved
		in waves: ruptures, manifestos, and revivals that reshape what film can be.
	</p>

