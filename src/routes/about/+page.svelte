	<h2>How it differs</h2>
	<p>
		Cinema Waves is excellent at the prose: each movement has a thoughtful essay. This site does
		something different — it puts the movements <em>on a map and a timeline</em>, lets you see them
		all at once, and threads the influence edges so you can follow how Italian Neorealism became the
		French New Wave became Cinema Novo became the Iranian New Wave.
	</p>

